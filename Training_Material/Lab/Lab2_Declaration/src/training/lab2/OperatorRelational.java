package training.lab2;
// OperatorRelational.java
class OperatorRelational {
	public static void main(String args[]) {
		int a = 0, b = 1;
		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a < b);
		System.out.println(a <= b);
		System.out.println(a > b);
		System.out.println(a >= b);
	}
}
